class Memberfeedback extends BaseMember {

    public function _initialize() {
        parent::_initialize(); // TODO: Change the autogenerated stub
        Lang::load(APP_PATH . 'home/lang/'.config('default_lang').'/memberfeedback.lang.php');
    }

    /*
     * 反馈列表
     */

    public function index() {
        $feedback_model = model('feedback');
        $condition = array(
            'member_id' => session('member_id')
        );
        $feedback_list = $feedback_model->getFeedbackList($condition,10);
        /* 设置买家当前菜单 */
        $this->setMemberCurMenu('member_inform');
        /* 设置买家当前栏目 */
        $this->setMemberCurItem('feedback_list');
        $this->assign('feedback_list', $feedback_list);
        $this->assign('show_page', $feedback_model->page_info->render());
        return $this->fetch($this->template_dir . 'index');
    }

    public function add(){
        if (request()->isPost()) {
            $feedback_model = model('feedback');
            $param = array();
            $param['fb_content'] = input('param.fb_content');
            $param['fb_type'] = 2;
            $param['fb_time'] = TIMESTAMP;
            $param['member_id'] = session('member_id');
            $param['member_name'] = session('member_name');

            $result = $feedback_model->addFeedback($param);

            if ($result) {
                ds_json_encode(10000, lang('ds_common_op_succ'));
            } else {
                ds_json_encode(10001, lang('ds_common_op_fail'));
            }
        }else{
            $this->setMemberCurMenu('member_inform');
            /* 设置买家当前栏目 */
            $this->setMemberCurItem('feedback_add');
            return $this->fetch($this->template_dir . 'add');
        }
    }

    /**
     * 用户中心右边，小导航
     *
     * @param string $menu_type 导航类型
     * @param string $menu_key 当前导航的menu_key
     * @param array $array 附加菜单
     *
     * @return
     */
    public function getMemberItemList() {
        $menu_array = array(
            array(
                'name' => 'feedback_list',
                'text' => lang('feedback_list'),
                'url' => url('Memberfeedback/index')
            ),
            array(
                'name' => 'feedback_add',
                'text' => lang('feedback_add'),
                'url' => url('Memberfeedback/add'),
            )
        );

        return $menu_array;
    }

}
